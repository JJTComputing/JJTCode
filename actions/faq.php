<h2>FAQ</h2>
<h3>What is this JJTCode?</h3>
<p>JJTCode is a code repository, like <a href="http://docs.google.com" target="_blank">Google Docs,</a> but for code. It allows you
to develop your application anywhere, on any computer with an internet connection and an internet browser, without having to
use an IDE, while still having syntax highlighting.</p>
<h3>How do I Use It?</h3>
<p>To use JJTCode, firstly you need to <a href="/?action=register">register.</a> Once you have confirmed your email address,
you will have an option on the <a href="/?action=project_view">projects</a> page to create a project.</p>
<h3>How Come I Can't Edit or View A File?</h3>
<p>If a file is not linked to the edit or view pages, this is because it is a binary file. Binary files cannot be
edited by JJTCode due to their very nature. You will need to download the file and open it on your own
computer if you wish to edit it.</p>
<h3>I'm Not Allowed to View a Project!</h3>
<p>Different projects have different permission levels. It could be that the project admins do not want
anyone to view their source code, and as such only the people they allow will be able to view it. Another possibility
is that you have been banned from viewing a project. You will need to contact a project admin to be able to
resolve this issue.</p>
